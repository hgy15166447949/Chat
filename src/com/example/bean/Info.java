package com.example.bean;



public class Info {
	public String name;
	public int icon;
	public String msgId;

	public String getMsgId() {
		return msgId;
	}

	public void setMsgId(String msgId) {
		this.msgId = msgId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getIcon() {
		return icon;
	}

	public void setIcon(int icLauncher) {
		this.icon = icLauncher;
	}

}


